           <h2 class="text-center"> MAXFIYLIK SIYOSATI</h2><br>
    
            Shaxsiy maʼlumotlarning maxfiylik siyosati (bundan keyin matnda Siyosat, deb yuritiladi) “innoweek.uz” rasmiy sayti (bundan keyin matnda innoweek.uz sayti, deb yuritiladi) innoweek.uz sayti yoki shu saytga aloqador boshqa mobil ilovalardan foydalanish chog‘ida foydalanuvchi haqida olinadigan maʼlumotlarga nisbatan qo‘llaniladi. innoweek.uz sayti O‘zbekiston Respublikasi qonunlariga binoan rasman ro‘yxatdan o‘tgan sayt hisoblanadi.
    
            innoweek.uz saytining har qanday resurslaridan foydalanish foydalanuvchi tomonidan mazkur Siyosat va unda ko‘rsatilgan shartlarga so‘zsiz rozi ekanligini anglatadi. Agar foydalanuvchi mazkur Siyosat shartlariga rozi bo‘lmagan taqdirda u innoweek.uz resurslaridan foydalanmasligi kerak. <br><br>
    
            <h5>1. TERMINLAR</h5>
    
            1 innoweek.uz sayti (bundan keyin matnda Sayt, deb yuritiladi) – bu kontentning serverdagi joylashuv o‘rnidir. <br><br>
    
            2. “innoweek.uz” rasmiy sayti – yangi innavatsion g`oyalar va  yangiliklarni ulashuvchi sayt. <br><br>
    
            3. Shaxsiy maʼlumotlar – muayyan jismoniy yoki yuridik shaxsga (shaxsiy maʼlumot subyekti) bevosita yoki bilvosita aloqador bo‘lgan har qanday maʼlumot  hisoblanadi. <br><br>
    
            4. Shaxsiy maʼlumotlarning maxfiyligi – jismoniy yoki yuridik shaxslarning maʼlumotlaridan foydalanish huquqiga ega bo‘lgan shaxslarning, boshqalarga taalluqli maʼlumotlardan ularning roziligisiz yoki qonuniy asoslarsiz foydalanmasligi va tarqatmasligi uchun majburiy talab hisoblanadi.
             <br><br>
    
            <h5>2. FOYDALANUVCHINING INNOWEEK.UZ SAYTI TOMONIDAN YIG‘ILADIGAN VA QAYTA ISHLANADIGAN SHAXSIY MAʼLUMOTI</h5> <br>
    
            5. Ushbu Maxfiylik siyosati foydalanuvchining saytdan ro‘yxatdan o‘tish, huquqiy hujjatlardan foydalanish chog‘ida sayt tomonidan so‘ralgan maʼlumotlar kiritilganda ushbu maʼlumotlarning maxfiyligini muhofaza qilish va oshkor etmaslik tartibini taʼminlashga qaratilgan innoweek.uz sayti maʼmuriyatining (bundan keyin matnda Maʼmuriyat deb yuritiladi) majburiyatlarini belgilaydi. <br><br>
    
            6. Ushbu Maxfiylik siyosati doirasida qayta ishlashga ruxsat berilgan shaxsiy maʼlumotlar Saytda ro‘yxatdan o‘tish chog‘ida jadvalni to‘ldirish orqali foydalanuvchi tomonidan mustaqil ravishda taqdim etiladi va u quyidagilarni o‘z ichiga oladi: Foydalanuvchining familiyasi, ismi-sharifi, foydalanuvchining telefon raqami, elektron pochta manzili (e-mail). Shuningdek, ishlov beriluvchi maʼlumotlarga Foydalanuvchi tomonidan mustaqil ravishda sayt maʼmuriyatiga yoki maʼmuriyatning iltimosiga ko‘ra taqdim etilgan har qanday qo‘shimcha maʼlumot kiradi. <br><br>
    
            <h5>SHAXSIY MAʼLUMOTLAR XAVFSIZLIGI</h5>
    
            7. Shaxsiy maʼlumotlarni noqonuniy xatti-harakatlardan, yo‘qolish, yo‘q qilish va uchinchi shaxslar tomonidan ruxsatsiz kirishdan himoya qilish uchun maʼmuriyat zamonaviy dasturiy taʼminot va apparatni himoya qilish vositalaridan hamda tashkiliy xavfsizlik choralaridan foydalanadi. <br>
    
            Maʼmuriyat maʼlumotlarning maxfiyligini taʼminlash uchun barcha imkoniyatlarni ishga solishni o‘z zimmasiga oladi. <br><br>
    
            Maʼmuriyat foydalanuvchining iltimosi yoki uchinchi shaxslarning daʼvolari bo‘yicha sud yoxud huquqni muhofaza qiluvchi organlarning so‘roviga asosan, maʼmuriyatning huquqlari va qonuniy manfaatlarini taʼminlash yuzasidan, shuningdek qonunchilikda belgilangan boshqa hollarda foydalanuvchining shaxsiy maʼlumotlariga kirish huquqiga ega. <br><br>
    
            <h5>MAXFIYLIK SIYOSATINI O‘ZGARTIRISH</h5>
    
            8. Maʼmuriyat ushbu Siyosatga mustaqil ravishda o‘zgartirish kiritish huquqiga ega. Siyosatning o‘zgartirilgan versiyasi Saytda eʼlon qilingan paytdan boshlab darhol kuchga kiradi va foydalanuvchi uchun majburiy hisoblanadi. Foydalanuvchilarga Siyosat o‘zgarishi to‘g‘risida maxsus xabar berish mumkin, ammo bunday ogohlantirish talab qilinmaydi va Maʼmuriyat barcha manfaatdor tomonlarni Siyosatdagi o‘zgarishlarni kuzatishga undaydi. Siyosatdagi o‘zgarishlarga rozi bo‘lmagan taqdirda, foydalanuvchi Saytdan foydalanishni to‘xtatishi shart.
    
